//显示图片
//x,y:起点坐标
//p_w:图片宽（单位像素）
//p_h:图片高（单位像素）
//*p:图片起始地址
void LCD_showPicture(uint8_t x,uint8_t y,uint8_t *p,uint8_t p_w,uint8_t p_h,uint16_t color)
{
    uint8_t temp,i,col,row;
    uint8_t y0=y;
    uint8_t width=p_w;
    if(x+p_w>LCD_W)width=LCD_W-p_w;//实际显示宽度
    uint8_t high=p_h;
    if(y+p_h>LCD_H)high=LCD_H-p_h;//实际显示高度
    uint8_t exp_col_bytes=(p_h/8+((p_h%8)?1:0));//显示一列的字节数
    uint8_t act_col_bytes=(high/8+((high%8)?1:0));//实际显示一列的字节数

    for(row=0; row<width; row++) //列++
    {
        for(col=0; col<act_col_bytes; col++) //显示一列
        {
            temp = p[col+row*exp_col_bytes];
            for(i=0; i<8; i++)
            {
                if(temp&0x80)LCD_DrawPoint(x,y,color);
                //else LCD_DrawPoint(x,y,color);
                temp<<=1;
                y++;
                if((y-y0)==high)
                {
                    y=y0;
                    x++;
                    break;
                }
            }
        }
    }
}

void LCD_Draw_Bitmap(int16_t x, int16_t y, uint8_t *bitmap, uint16_t p_w, uint16_t p_h,uint16_t color)
{
    int16_t x0 = x;
    uint16_t col_bytes = (p_w >> 3) + ((p_w % 8) ? 1 : 0); //一行字节数

    for (int col_i = 0; col_i < p_h; col_i++) //行++
    {
        for (int col = 0; col < col_bytes; col++) //显示一行
        {
            uint8_t temp = bitmap[col_i * col_bytes + col];

            for (int i = 0; i < 8; i++) // 显示一个字节
            {

                if ((x >= 0) && (y >= 0) && (x < LCD_W) && (y < LCD_H))
                {
                    if(temp&0x80)
                        LCD_DrawPoint(x,y,color);
                }
                temp <<= 1;
                x++;
                if ((x - x0) == p_w) /* 显示达到宽度或大于限制窗口，换行 */
                {
                    x = x0;
                    y++;                           /* 指向下一行（逐行式） */
                    break;
                }
            }

            if (x == x0) break; //换行
        }
    }
}
