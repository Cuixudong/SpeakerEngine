{
    while((*p<='~')&&(*p>=' '))//判断是不是非法字符!
    {
        if(x>(LCD_W-(size/2))) {
            x=0;
            y+=size;
        }
        if(y>(LCD_H-size)) {
            y=x=0;
        }
        LCD_ShowChar(x,y,*p,size,color);
        x+=size/2;
        p++;
    }

}

//显示一个指定大小的汉字
//x,y :汉字的坐标
//font:汉字GBK码
//f_w:字宽
//f_h:字高
//mode:0,正常显示,1,叠加显示
void show_font(uint16_t x,uint16_t y,const char *font,uint8_t f_w,uint8_t f_h,uint16_t color)
{
    const char* font_table;
    uint16_t fontSeq;
    uint8_t temp,t,t1;
    uint16_t y0=y;
    uint8_t csize=(f_h/8+((f_h%8)?1:0))*f_w;//得到自由分辨字符所占的字节数

    if(f_w==16 && f_h==16)
        font_table = font1616_table;
    else return;/*没有的字库*/
    for(fontSeq=0; fontSeq<strlen(font_table)/2; fontSeq++)/*计算font_table对应字库的数组下标*/
    {
        if(font_table[2*fontSeq]==font[0] && font_table[2*fontSeq+1]==font[1])
            break;
    }
    if(fontSeq >= strlen(font_table)/2) return;/*font_table中没有font该字*/

    for(t=0; t<csize; t++)
    {
        if(f_w==16 && f_h==16)
            temp = font_1616[fontSeq][t];/*调用font_1616字库*/
        for(t1=0; t1<8; t1++)
        {
            if(temp&0x80)LCD_DrawPoint(x,y,color);
            //else LCD_DrawPoint(x,y,!color);
            temp<<=1;
            y++;
            if((y-y0)==f_h)
            {
                y=y0;
                x++;
                break;
            }
        }
    }
}

//在指定位置开始显示一个字符串
//支持自动换行
//(x,y):起始坐标
//width,height:区域
//str  :字符串
//f_w:字宽
//f_h:字高
//mode:0,非叠加方式;1,叠加方式
void show_str(uint16_t x,uint16_t y,char *str,uint8_t f_h,uint16_t color)
{
    uint16_t x0=x;
    uint16_t y0=y;
    uint8_t bHz=0;     //字符或者中文
    while(*str!=0)//数据未结束
    {
        if(!bHz)
        {
            if((uint8_t)*str>0x80)bHz=1;//中文
            else               //字符
            {
                if(x>(x0+LCD_W-f_h/2))//换行
                {
                    y+=f_h;
                    x=x0;
                }
                if(y>(y0+LCD_H-f_h))break;//越界返回
                if(*str==13)//换行符号
                {
                    y+=f_h;
                    x=x0;
                    str++;
                }
                else LCD_ShowChar(x,y,*str,f_h,color);//有效部分写入
                str++;
                x+=f_h/2; //字符,为全字的一半
            }
        }
        else//中文
        {
            bHz=0;//有汉字库
            if(x>(x0+LCD_W-f_h))//换行
            {
                y+=f_h+2;//2为行间距
                x=x0;
            }
            if(y>(y0+LCD_H-f_h))
                break;//越界返回
            show_font(x,y,str,f_h,f_h,color); //显示这个汉字,空心显示
            str+=2;
            x+=f_h;//下一个汉字偏移
        }
    }
}
//在指定宽度的中间显示字符串
//如果字符长度超过了len,则用Show_Str显示
//len:指定要显示的宽度
void show_str_mid(uint16_t x,uint16_t y,char *str,uint8_t f_h,uint16_t color,uint16_t len)
{
    uint16_t strlenth=0;
    strlenth=strlen((char*)str);
    strlenth*=f_h/2;
    if(strlenth>len)show_str(x,y,str,f_h,color);
    else
    {
        strlenth=(len-strlenth)/2;
        show_str(strlenth+x,y,str,f_h,color);
    }
}

